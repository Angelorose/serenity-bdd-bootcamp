package Polymorphism;

public class Animal {
	
	public void eat(String sKind) {
		System.out.println(sKind+" is eating");
	}
	public void hunt() {
		System.out.println("Animal is hunting");
	}
	public void hunt2() {
		System.out.println("Animal is hunting");
	}
	public void speak() {
		System.out.println("Animal is speaking");
		
	}
		

}
